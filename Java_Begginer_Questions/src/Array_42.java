// WAP to create a 1D array with ten elements and print it.

public class Array_42 {
    public static void main(String[] args) {
        int[] arr = { 10,20,30,40,50,60,70,80,90,10};
        System.out.println("Stored elements are: ");
        for (int i = 0; i<10; i++){
            System.out.print(arr[i]+" ");
        }
    }
}
